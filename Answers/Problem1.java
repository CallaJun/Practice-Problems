public class Problem1 {

	public static void main(String[] args) {
		/*
		 * This for loop initializes an integer i which starts at 2,
		 * and loops while i is less than or equal to 20.
		 * Each time the for loop runs, i increments by 2.
		 */
		for (int i = 2; i <= 20; i += 2) {
			// Prints out i
			System.out.println(i);
		}
	}

}
